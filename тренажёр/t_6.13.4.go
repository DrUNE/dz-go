package main

import "fmt"

// **Описание**: Реализуйте программу, которая итерирует по слайсу целых чисел
// с помощью for ... range и выводит сумму всех его элементов.
//
// **Входные данные**: Данные встроены в программу. Используйте следующий слайс:
// - []int{3, 8, 2, 11, 5, 7}
//
// **Выходные данные**: Одна строка — сумма всех элементов слайса.
//
// **Ограничения**:
// - Использовать только for ... range для итерации по слайсу
// - Не использовать обычный цикл for с индексом (только range)
// - Использовать только fmt.Println для вывода
// - Не использовать make, append, len, cap
//
// **Примеры**:
// Input: (данные встроены в программу)
// Output:
// 36

func main() {
	nums := []int{3, 8, 2, 11, 5, 7}

	// Ваш код здесь
	sum := 0
	for _, num := range nums {
		sum += num
	}
	fmt.Println(sum)
}
