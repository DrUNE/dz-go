package main

import "fmt"

// **Описание**: Создайте программу, которая создаёт пустой map с заранее выделенной ёмкостью с помощью make, добавляет в него несколько новых пар ключ-значение и выводит итоговое количество элементов.
//
// **Входные данные**: В коде задать:
// - Тип map: map[string]int для хранения количества студентов по курсам
// - Ёмкость при создании: 10
// - Пары для добавления:
//   - "golang" -> 25
//   - "python" -> 40
//   - "javascript" -> 35
//
// **Выходные данные**: Одна строка в формате:
// количество курсов: <число>
//
// **Ограничения**:
// - Обязательно создать map через make с указанием ёмкости 10
// - Добавить ровно 3 пары ключ-значение, указанные выше
// - Для получения количества элементов использовать len
// - Ключи и значения задать как константы в коде

func main() {
	statsMap := make(map[string]int, 10)
	statsMap["golang"] = 25
	statsMap["python"] = 40
	statsMap["javascript"] = 35
	fmt.Printf("количество курсов: %d\n", len(statsMap))
}
