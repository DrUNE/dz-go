// **Описание**: Реализуйте программу, которая создаёт слайс из массива с помощью опущенных границ
// диапазона и выводит элементы каждого полученного слайса.
//
// **Входные данные**: Данные встроены в программу. Используйте следующие параметры:
// - Массив: [6]int{10, 20, 30, 40, 50, 60}
// - Первый слайс: элементы от начала до индекса 3 (не включая 3)
// - Второй слайс: элементы начиная с индекса 3 до конца
// - Третий слайс: все элементы массива
//
// **Выходные данные**: Три строки — элементы каждого слайса через пробел:
// 1. Элементы первого слайса
// 2. Элементы второго слайса
// 3. Элементы третьего слайса
//
// **Ограничения**:
// - Использовать только опущенные границы диапазона ([:3], [3:], [:])
// - Не использовать make, append, len, cap
// - Использовать только fmt.Println для вывода
//
// **Примеры**:
// Input: (данные встроены в программу)
// Output:
// 10 20 30
// 40 50 60
// 10 20 30 40 50 60

func main() {
	// Ваш код здесь
	arr := [6]int{10, 20, 30, 40, 50, 60}
	slices := [][]int{arr[:3], arr[3:], arr[:]}
	for _, slice := range slices {
		fmt.Println(strings.Join(intsToStrings(slice), " "))
	}
}

func intsToStrings(nums []int) []string {
	result := make([]string, len(nums))
	for i, n := range nums {
		result[i] = strconv.Itoa(n)
	}
	return result
}
