package main

import "fmt"

// **Описание**: Создайте программу, которая работает со словарём переводов слов
// с английского на русский. Используйте предоставленный map для получения
// перевода конкретного слова и вывода результата.
//
// **Входные данные**: Готовый map с переводами (встроен в код),
// слово для поиска перевода: "dog"
//
// **Выходные данные**: Строка вида `Перевод слова 'dog': собака`
//
// **Ограничения**:
// - Использовать готовый map из шаблона
// - Не изменять исходные данные map
// - Слово для поиска задано как константа в коде

func main() {
	translations := map[string]string{
		"cat":  "кошка",
		"dog":  "собака",
		"bird": "птица",
		"fish": "рыба",
	}

	word := "dog"

	fmt.Println("Перевод слова '" + word + "': " + translations[word])
}
