package main

import "fmt"

// **Описание**: Напишите программу, которая проходит по всем элементам map с ценами товаров
// и выводит каждую пару ключ-значение в указанном формате.
//
// **Входные данные**: Готовый map с ценами товаров (встроен в код)
//
// **Выходные данные**: Для каждой пары ключ-значение из map вывести отдельную строку в формате:
// товар: <название>, цена: <цена>
//
// **Ограничения**:
// - Использовать готовый map из шаблона
// - Не изменять исходные данные map
// - Порядок вывода строк может быть любым (map в Go не гарантирует порядок)

func main() {
	prices := map[string]int{
		"bread":  45,
		"milk":   80,
		"cheese": 350,
		"butter": 210,
	}

	for key, value := range prices {
		fmt.Println("товар: " + key + ", цена: " + fmt.Sprint(value))
	}
}
