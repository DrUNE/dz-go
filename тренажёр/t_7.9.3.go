package main

import "fmt"

// **Описание**: Создайте программу, которая удаляет заданный элемент из map городов и их населения, а затем выводит длину map до и после удаления.
//
// **Входные данные**: Готовый map с населением городов (встроен в код):
// population := map[string]int{
//     "Moscow": 12000000,
//     "Kazan": 1250000,
//     "Sochi": 450000,
//     "Omsk": 1140000,
// }
// Ключ для удаления: "Sochi"
//
// **Выходные данные**: Две строки в формате:
// длина до удаления: <число>
// длина после удаления: <число>
//
// **Ограничения**:
// - Использовать готовый map из шаблона
// - Ключ для удаления задан как константа в коде
// - Не создавать новый map, работать с исходным
//
// **Примеры**:
// Input: map population с 4 элементами, ключ "Sochi"
// Output:
// длина до удаления: 4
// длина после удаления: 3

func main() {
	population := map[string]int{
		"Moscow": 12000000,
		"Kazan":  1250000,
		"Sochi":  450000,
		"Omsk":   1140000,
	}
	keyToDelete := "Sochi"

	fmt.Printf("длина до удаления: %d\n", len(population))
	delete(population, keyToDelete)
	fmt.Printf("длина после удаления: %d\n", len(population))
}
