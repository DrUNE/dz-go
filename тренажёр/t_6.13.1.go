package main

import "fmt"

// **Описание**: Реализуйте программу, которая использует make для создания слайса
// с заранее заданной ёмкостью, добавляет в него несколько элементов с помощью append,
// а затем выводит длину и ёмкость слайса до и после добавления элементов.
//
// **Входные данные**: Данные встроены в программу.
// - Создайте слайс типа []int с длиной 0 и ёмкостью 4
// - Добавьте в слайс три элемента: 7, 14, 21
//
// **Выходные данные**: Четыре строки вывода:
// 1. Длина и ёмкость слайса сразу после создания через make
// 2. Длина и ёмкость слайса после добавления всех трёх элементов
// Формат каждой строки: два числа через пробел — сначала len, затем cap.
//
// **Ограничения**:
// - Использовать только make, append, len(), cap() и fmt.Println
// - Не создавать дополнительных слайсов или массивов
// - Ёмкость при создании строго равна 4, длина строго равна 0
//
// **Пример вывода**:
// 0 4
// 3 4

func main() {
	// Ваш код здесь
	mySlice := make([]int, 0, 4)
	fmt.Println(len(mySlice), cap(mySlice))
	mySlice = append(mySlice, 7, 14, 21)
	fmt.Println(len(mySlice), cap(mySlice))
}
