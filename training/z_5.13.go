package main

import (
	"fmt"
)

// **Описание**: Реализуйте функцию checkTemperature, которая принимает целое число —
// температуру в градусах Цельсия — и возвращает ошибку, если температура выходит
// за пределы допустимого диапазона от -50 до 50 включительно.
// В main вызовите функцию с заданным значением и выведите результат.
//
// **Входные данные**: Входные данные не требуются — значение температуры задано
// в программе переменной temp со значением 72.
//
// **Выходные данные**: Одна строка — сообщение об ошибке с указанием значения,
// если температура вне диапазона, или строка «Температура допустима: [значение]»
// если температура в допустимом диапазоне.
//
// **Ограничения**:
// - Реализовать функцию checkTemperature(temp int) error
// - Использовать пакет errors для создания ошибки
// - Проверять условие выхода за диапазон [-50, 50] внутри функции
// - В main проверять возвращённую ошибку через if err != nil
// - Не использовать циклы, switch, массивы или слайсы
//
// **Примеры**:
// Input: temp = 72
// Output: Ошибка: температура 72 вне допустимого диапазона
//
// Input: temp = -30
// Output: Температура допустима: -30

func checkTemperature(temp int) error {
	if temp < -50 || temp > 50 {
		return fmt.Errorf("температура %d вне допустимого диапазона", temp)
	} else {
		return nil
	}
}

func main() {
	temp := 72
	err := checkTemperature(temp)
	if err == nil {
		fmt.Printf("Температура допустима: %d\n", temp)
	} else {
		fmt.Printf("Ошибка: %v\n", err)
	}
}
